/** Define the possible HTTP methods
 - get: Represents a GET request
 - post: Represents a POST request
 - put: Represents a PUT request
 - delete: Represents a DELETE request
 */
public enum HTTPMethod: String {
    case get = "GET"
    case post = "POST"
    case put = "PUT"
    case delete = "DELETE"
}
